package org.example.Inheritance.Calculation;

public class Calulator {
    private int z;

    //addition
    public void add(int y,int x)
    {
        z = y + x;

        System.out.println("The sum of the two inputs is " + z);
    }

    //subtraction
    public void sub(int a, int b)
    {
        z = a - b;
        System.out.println("The sum of the two inputs is " + z);
    }


}
